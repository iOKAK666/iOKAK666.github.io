Data types
The data type of the value may be either a value type (set of values, one of which the variable directly contains) or a reference type (where a value, usually of a composite data type, is referenced by the variable rather than being contained, and the reference is the value being stored).

Value types allow an assignment to copy the value to another variable, while reference types do not; instead the assignment copies the reference. In other words, two reference type values created from the same constructor with the same given values are not equal since their references are different (the rawequal function will return false). Consequently, value types can be used to index a table via same values while reference types cannot; only references can be used.

The type function can determine a value's type and return one of the nine types (nil, boolean, number, string, function, table, thread, vector, and userdata) as a string. Value types include nil, boolean, number, string and vector. Reference types include function, table, thread and userdata. The typeof function includes Roblox types which are userdata.

