Identifier
An identifier is a lexical token which is used to access variables. It can be any string of letters, digits, and underscores, but does not start with a digit. Identifiers are case-sensitive; foo and Foo are two different identifiers for example. Identifiers are also used in table fields, preceded by a period (.), and in table constructors as keys.
Local variables
A local variable can be declared using the local statement followed by an assignment statement or just the identifier itself. In contrast, a global variable is a variable that is not explicitly declared as local. The statement scopes the variable so that it can only be used from inside its scope. It may also be used as an upvalue, i.e., used in a function along with its local variables unless if another variable with the same name is declared local within the function.

There can only be 200 locals/upvalues at a time.[1] Luau has 255 registers since an instruction only has 8 bits to refer to one, hence the limits. Because local variables require less instructions than global variables, using local variables provides faster performance than using global variables.[2]

