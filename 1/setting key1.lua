Engine API Reference Page Links
When referencing a Roblox Engine API member, such as a class, method, enum, data type, global function, or library, use our special API link syntax instead.

Usage Guidelines
In prose, you should always link to an API member, but avoid excessive usage through pronouns and contextual references. For example:

BAD: BasePart is an abstract base class for in-world objects that render and are physically simulated. BaseParts can be grouped into simulated rigid assemblies, and BaseParts can detect collisions with other BaseParts.
BETTER: BasePart is an abstract base class for in-world objects that render and are physically simulated. BaseParts can be grouped into simulated rigid assemblies and they can detect collisions with each other.
Never frame API names in the possessive; instead, adjust the phrasing to avoid possessive case:

BAD: A BasePart's color is defined by its Color property.
BETTER: The color of a BasePart is defined by its Color property.
If a term should be plural in prose, append the plural term after a | as outlined in Link Substitution.

BAD: A model can contain several BaseParts.
BETTER: A model can contain several BaseParts.
For words that are both concepts and API terms (for example, a Touched event), consider whether you're referring to the concept or the term. Concepts require no special formatting.

API TERM: Connect the function to the Touched event.
CONCEPT: Display text after a player's character has touched the part.
