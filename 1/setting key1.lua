local function SiftUp(heap, index)
	local parentIndex
	if index ~= 1 then
		parentIndex = Floor(index/2)
		if heap.Compare(heap[parentIndex], heap[index]) then
			heap[parentIndex], heap[index] = heap[index], heap[parentIndex]
			SiftUp(heap, parentIndex)
		end
	end
